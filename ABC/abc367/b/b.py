def myin():
    return stdin.readline().rstrip()

def myin_sp():
    return stdin.readline().rstrip().split()

def myin_sp_i():
    return list(map(int,myin_sp()))

def myin_sp_s():
    return list(map(str,myin_sp()))

def main():
    N = list(myin())
    while N[-1]=="0":
        N.pop()
    if N[-1]==".":
        N.pop()
    print("".join(N))
if __name__ == "__main__":
    main()